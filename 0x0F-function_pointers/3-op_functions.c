#include "3-calc.h"

/**
 * op_add - adds
 * @a: input
 * @b: input
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub
 * @a: input
 * @b: input
 * Return: int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul 2 nmbs
 * @a: input
 * @b:input
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides
 * @a: input
 * @b: input
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo of 2
 * @a: input
 * @b: input
 * Return: int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
